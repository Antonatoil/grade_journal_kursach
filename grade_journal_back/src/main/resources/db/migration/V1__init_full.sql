-- 10. Учебные назначения дисциплин группам
-- ---------------------------------------------------------------------
with current_term as (
    select term_id
    from academic_term
    where is_current = true
),
curriculum as (
    select
        sg.group_id,
        c.course_id,
        row_number() over (partition by sg.group_id order by c.course_id) as course_rank
    from study_group sg
    join course c on c.study_year = sg.course_no
)
insert into course_offering (course_id, teacher_id, group_id, term_id, status)
select
    cur.course_id,
    ((cur.group_id + cur.course_rank - 2) % 15) + 1 as teacher_id,
    cur.group_id,
    ct.term_id,
    'active'
from curriculum cur
cross join current_term ct
order by cur.group_id, cur.course_id;

-- ---------------------------------------------------------------------
-- 11. Расписание
-- 4 дисциплины на группу, по 12 занятий на каждую
-- Чтобы уже сейчас было достаточно прошедших занятий для 3-8 оценок
-- ---------------------------------------------------------------------
with current_term as (
    select
        term_id,
        greatest(start_date, current_date - 36) as base_date
    from academic_term
    where is_current = true
),
offering_rank as (
    select
        co.offering_id,
        co.group_id,
        c.course_name,
        row_number() over (partition by co.group_id order by c.course_id) as course_rank
    from course_offering co
    join course c on c.course_id = co.course_id
    join current_term ct on ct.term_id = co.term_id
)
insert into schedule_entry (offering_id, lesson_date, time_slot, lesson_type, room, topic)
select
    orr.offering_id,
    ct.base_date + (((orr.course_rank - 1)::int) + ((gs.lesson_idx)::int * 3)) as lesson_date,
    case orr.course_rank
        when 1 then '09:00-10:20'
        when 2 then '10:35-11:55'
        when 3 then '12:25-13:45'
        else        '14:00-15:20'
    end as time_slot,
    case
        when gs.lesson_idx in (0, 3, 6, 9) then 'lecture'
        when gs.lesson_idx in (1, 4, 7, 10) then 'practice'
        else 'lab'
    end as lesson_type,
    'А-' || lpad((100 + ((orr.group_id + orr.course_rank::int + gs.lesson_idx::int) % 20))::text, 3, '0') as room,
    'Занятие ' || (gs.lesson_idx + 1) || ': ' || orr.course_name as topic
from offering_rank orr
cross join current_term ct
cross join generate_series(0, 11) as gs(lesson_idx)
order by orr.offering_id, lesson_date;

