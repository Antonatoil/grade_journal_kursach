    current_date,
    'active'
from student s
join course_offering co on co.group_id = s.group_id
order by s.student_id, co.offering_id;

-- ---------------------------------------------------------------------
-- 13. Контрольные точки
-- По каждой дисциплине создаем 8 оценочных точек.
-- Первые 4 обязательные, остальные дополнительные.
-- ---------------------------------------------------------------------
with ranked_schedule as (
    select
        se.schedule_id,
        se.offering_id,
        se.lesson_date,
        row_number() over (
            partition by se.offering_id
            order by se.lesson_date, se.time_slot
        ) as lesson_no
    from schedule_entry se
),
first_eight as (
    select *
    from ranked_schedule
    where lesson_no <= 8
)
insert into assessment_item (
    offering_id,
    assessment_type_id,
    title,
    max_score,
    weight,
    due_date,
    schedule_id,
    is_required
)
select
    fe.offering_id,
    case fe.lesson_no
        when 1 then (select assessment_type_id from assessment_type where type_code = 'quiz')
        when 2 then (select assessment_type_id from assessment_type where type_code = 'lab')
        when 3 then (select assessment_type_id from assessment_type where type_code = 'quiz')
        when 4 then (select assessment_type_id from assessment_type where type_code = 'test')
        when 5 then (select assessment_type_id from assessment_type where type_code = 'lab')
        when 6 then (select assessment_type_id from assessment_type where type_code = 'quiz')
        when 7 then (select assessment_type_id from assessment_type where type_code = 'lab')
        when 8 then (select assessment_type_id from assessment_type where type_code = 'exam')
    end as assessment_type_id,
    case fe.lesson_no
        when 1 then 'Краткий тест 1'
        when 2 then 'Лабораторная работа 1'
        when 3 then 'Краткий тест 2'
        when 4 then 'Контрольная работа'
        when 5 then 'Лабораторная работа 2'
        when 6 then 'Краткий тест 3'
        when 7 then 'Лабораторная работа 3'
        when 8 then 'Итоговая работа'
    end as title,
    10.00,
    case fe.lesson_no
        when 1 then 0.08
        when 2 then 0.12
        when 3 then 0.08
        when 4 then 0.18
        when 5 then 0.12
        when 6 then 0.08
        when 7 then 0.14
        when 8 then 0.20
    end as weight,
    fe.lesson_date,
    fe.schedule_id,
    case
        when fe.lesson_no <= 4 then true
        else false
    end as is_required
from first_eight fe
order by fe.offering_id, fe.lesson_no;

-- ---------------------------------------------------------------------
-- 14. Посещаемость
-- Делаем живой разброс посещаемости по всем прошедшим занятиям
-- При этом у каждого студента по предмету будет от 3 до 8 оценок
-- ---------------------------------------------------------------------
with ranked_schedule as (
    select
        se.schedule_id,
        se.offering_id,
        se.lesson_date,
        row_number() over (
            partition by se.offering_id
            order by se.lesson_date, se.time_slot
        ) as lesson_no
    from schedule_entry se
    where se.lesson_date < current_date
),
targets as (
    select
        e.enrollment_id,
        e.student_id,
        co.course_id,
        e.offering_id,
        3 + ((e.student_id + co.course_id) % 6) as target_grade_count
    from enrollment e
    join course_offering co on co.offering_id = e.offering_id
)
insert into attendance (enrollment_id, schedule_id, is_present, note)
select
    t.enrollment_id,
    rs.schedule_id,
    case
        when rs.lesson_no <= t.target_grade_count then true
        when rs.lesson_no <= 8 and ((t.student_id + t.course_id + rs.lesson_no) % 3) = 0 then false
        when rs.lesson_no > 8 and ((t.student_id + t.course_id + rs.lesson_no) % 7) = 0 then false
        else true
    end as is_present,
    case
        when rs.lesson_no <= 8 and rs.lesson_no > t.target_grade_count and ((t.student_id + t.course_id + rs.lesson_no) % 3) = 0
            then 'Пропуск контрольного занятия'
        when rs.lesson_no > 8 and ((t.student_id + t.course_id + rs.lesson_no) % 7) = 0
            then 'Пропуск обычного занятия'
        else null
    end as note
from targets t
join ranked_schedule rs on rs.offering_id = t.offering_id
on conflict (enrollment_id, schedule_id) do nothing;

-- ---------------------------------------------------------------------
-- 15. Оценки
-- У каждого студента по каждому предмету будет от 3 до 8 оценок
-- ---------------------------------------------------------------------
with ranked_items as (
    select
        ai.assessment_item_id,
        ai.offering_id,
        ai.schedule_id,
        ai.due_date,
        row_number() over (
            partition by ai.offering_id
            order by ai.due_date, ai.assessment_item_id
        ) as point_no
    from assessment_item ai
),
targets as (
    select
        e.enrollment_id,
        e.student_id,
        co.course_id,
        e.offering_id,
        3 + ((e.student_id + co.course_id) % 6) as target_grade_count
    from enrollment e
    join course_offering co on co.offering_id = e.offering_id
)
insert into grade (
    enrollment_id,
    assessment_item_id,
    grade_value,
    teacher_comment,
    graded_at
)
select
    t.enrollment_id,
    ri.assessment_item_id,
    round(
        greatest(
            3::numeric,
            least(
                10::numeric,
                (
                    4
                    + ((t.student_id + t.course_id + ri.point_no) % 4)
                    + case
                        when t.target_grade_count >= 6 then 1
                        else 0
                      end
                    + case
                        when ((t.student_id + t.course_id) % 3) = 0
                             and ri.point_no >= greatest(t.target_grade_count - 1, 1) then 1
                        else 0
                      end
                    - case
                        when ((t.student_id + t.course_id) % 4) = 0
                             and ri.point_no = t.target_grade_count then 1
                        else 0
                      end
                )::numeric
            )
        ),
        0
    ) as grade_value,
    case
        when ((t.student_id + t.course_id) % 3) = 0 and ri.point_no = t.target_grade_count
            then 'Есть положительная динамика по последним работам'
        when ((t.student_id + t.course_id) % 4) = 0 and ri.point_no = t.target_grade_count
            then 'Последняя работа слабее предыдущих'
        when t.target_grade_count <= 4 and ri.point_no = t.target_grade_count
            then 'Нужно усилить подготовку по следующим занятиям'
        else '—'
    end as teacher_comment,
    (ri.due_date::timestamp + interval '18 hour')::timestamptz
from targets t
join ranked_items ri
    on ri.offering_id = t.offering_id
   and ri.point_no <= t.target_grade_count
join attendance att
    on att.enrollment_id = t.enrollment_id
   and att.schedule_id = ri.schedule_id
   and att.is_present = true
order by t.enrollment_id, ri.point_no
on conflict (enrollment_id, assessment_item_id) do nothing;

-- ---------------------------------------------------------------------
-- 16. Все оценки делаем целыми
-- ---------------------------------------------------------------------
update grade
set grade_value = round(grade_value, 0);

-- ---------------------------------------------------------------------
-- 17. Пересчитываем пропущенные часы с нуля
-- ---------------------------------------------------------------------
with missed as (
    select
        e.enrollment_id,
        coalesce(count(*) filter (where a.is_present = false) * 2, 0) as missed_hours_value
    from enrollment e
    left join attendance a on a.enrollment_id = e.enrollment_id
    group by e.enrollment_id
)
update enrollment e
set missed_hours = m.missed_hours_value
from missed m
where e.enrollment_id = m.enrollment_id;

-- ---------------------------------------------------------------------
-- 18. Прогноз итоговых результатов
-- Более детальная и живая логика
-- ---------------------------------------------------------------------
with grade_stats as (
    select
        e.enrollment_id,
        coalesce(round(avg(g.grade_value)::numeric, 2), 0.00) as avg_grade,
        count(g.grade_id) as graded_items
    from enrollment e
    left join grade g on g.enrollment_id = e.enrollment_id
    group by e.enrollment_id
),
attendance_stats as (
    select
        e.enrollment_id,
        count(a.attendance_id) as total_lessons,
        count(a.attendance_id) filter (where a.is_present) as present_lessons
    from enrollment e
    left join attendance a on a.enrollment_id = e.enrollment_id
    group by e.enrollment_id
),
required_stats as (
    select
        e.enrollment_id,
        count(ai.assessment_item_id) filter (
            where ai.is_required = true
              and ai.due_date < current_date
        ) as expected_items,
        count(g.grade_id) filter (
            where ai.is_required = true
              and ai.due_date < current_date
        ) as completed_items
    from enrollment e
    left join assessment_item ai
        on ai.offering_id = e.offering_id
    left join grade g
        on g.enrollment_id = e.enrollment_id
       and g.assessment_item_id = ai.assessment_item_id
    group by e.enrollment_id
),
recent_grades as (
    select
        x.enrollment_id,
        string_agg(x.grade_value::text, ', ' order by x.graded_at desc, x.grade_id desc) as last_three_grades
    from (
        select
            g.enrollment_id,
            g.grade_id,
            g.grade_value,
            g.graded_at,
            row_number() over (
                partition by g.enrollment_id
                order by g.graded_at desc, g.grade_id desc
            ) as rn
        from grade g
    ) x
    where x.rn <= 3
    group by x.enrollment_id
),
trend_stats as (
    select
        z.enrollment_id,
        max(case when z.rn_asc = 1 then z.grade_value end) as first_grade,
        max(case when z.rn_desc = 1 then z.grade_value end) as last_grade,
        count(*) as grade_count
    from (
        select
            g.enrollment_id,
            g.grade_value,
            row_number() over (
                partition by g.enrollment_id
                order by g.graded_at asc, g.grade_id asc
            ) as rn_asc,
            row_number() over (
                partition by g.enrollment_id
                order by g.graded_at desc, g.grade_id desc
            ) as rn_desc
        from grade g
    ) z
    group by z.enrollment_id
),
base as (
    select
        e.enrollment_id,
        co.course_id,
        c.course_name,
        coalesce(gs.avg_grade, 0.00) as current_average,
        coalesce(gs.graded_items, 0) as graded_items,
        round(
            case
                when coalesce(ast.total_lessons, 0) = 0 then 100.00
                else ast.present_lessons::numeric * 100.00 / ast.total_lessons
            end,
            2
        ) as attendance_rate,
        e.missed_hours,
        greatest(coalesce(rs.expected_items, 0) - coalesce(rs.completed_items, 0), 0) as missing_required_count,
        coalesce(rg.last_three_grades, 'нет данных') as last_three_grades,
        case
            when coalesce(ts.grade_count, 0) = 0 then 'нет данных'
            when coalesce(ts.grade_count, 0) = 1 then 'недостаточно данных'
            when ts.last_grade >= ts.first_grade + 1 then 'рост'
            when ts.last_grade <= ts.first_grade - 1 then 'снижение'
            else 'стабильность'
        end as trend,
        round(
            least(
                10.00::numeric,
                greatest(
                    1.00::numeric,
                    (
                        (case when coalesce(gs.avg_grade, 0) = 0 then 6.00 else gs.avg_grade end) * 0.82
                        + (
                            case
                                when coalesce(ast.total_lessons, 0) = 0 then 1.00
                                else ast.present_lessons::numeric / ast.total_lessons
                            end
                          ) * 1.80
                        - greatest(coalesce(rs.expected_items, 0) - coalesce(rs.completed_items, 0), 0) * 0.40
                        - e.missed_hours * 0.03
                        + case
                            when coalesce(ts.grade_count, 0) >= 2 and ts.last_grade >= ts.first_grade + 1 then 0.40
                            when coalesce(ts.grade_count, 0) >= 2 and ts.last_grade <= ts.first_grade - 1 then -0.40
                            else 0.00
                          end
                    )::numeric
                )
            ),
            0
        ) as predicted_final_grade,
        round(
            least(
                0.95::numeric,
                (
                    0.55
                    + least(coalesce(ast.total_lessons, 0), 12) * 0.015
                    + least(coalesce(gs.graded_items, 0), 8) * 0.03
                )::numeric
            ),
            2
        ) as confidence_score
    from enrollment e
    join course_offering co on co.offering_id = e.offering_id
    join course c on c.course_id = co.course_id
    left join grade_stats gs on gs.enrollment_id = e.enrollment_id
    left join attendance_stats ast on ast.enrollment_id = e.enrollment_id
    left join required_stats rs on rs.enrollment_id = e.enrollment_id
    left join recent_grades rg on rg.enrollment_id = e.enrollment_id
    left join trend_stats ts on ts.enrollment_id = e.enrollment_id
)
insert into performance_prediction (
    enrollment_id,
    current_average,
    attendance_rate,
    missed_hours,
    predicted_final_grade,
    risk_level,
    confidence_score,
    missing_required_count,
    recommendation_summary,
    model_version,
    generated_at
)
select
    b.enrollment_id,
    b.current_average,
    b.attendance_rate,
    b.missed_hours,
    b.predicted_final_grade,
    case
        when b.graded_items = 0
          or b.current_average < 6.00
          or b.attendance_rate < 70.00
          or b.missing_required_count >= 2 then 'high'
        when b.current_average < 7.50
          or b.attendance_rate < 85.00
          or b.missing_required_count = 1 then 'medium'
        else 'low'
    end as risk_level,
    b.confidence_score,
    b.missing_required_count,
    case
        when b.graded_items = 0 then
            'По дисциплине "' || b.course_name || '" пока нет ни одной оценки. Посещаемость: ' || b.attendance_rate
            || '%, пропущено часов: ' || b.missed_hours || '. Нужно начать с выполнения ближайшей контрольной точки.'
        else
            'По дисциплине "' || b.course_name || '" средний балл ' || b.current_average
            || ', посещаемость ' || b.attendance_rate || '%, пропущено часов: ' || b.missed_hours
            || '. Последние оценки: ' || b.last_three_grades
            || '. Текущая динамика: ' || b.trend
            || '. Прогнозная следующая оценка: ' || b.predicted_final_grade || '.'
    end as recommendation_summary,
    'init-v2-live',
    now()
from base b
order by b.enrollment_id;

