-- ---------------------------------------------------------------------
-- 1. Расширения
-- ---------------------------------------------------------------------
create extension if not exists pgcrypto;

-- ---------------------------------------------------------------------
-- 2. Справочники и учетные записи
-- ---------------------------------------------------------------------
create table role (
    role_id      integer generated by default as identity primary key,
    role_code    varchar(20) not null unique,
    role_name    varchar(50) not null
);

create table user_account (
    user_account_id  integer generated by default as identity primary key,
    role_id          integer not null references role(role_id),
    username         varchar(50) not null unique,
    password_hash    varchar(100) not null,
    full_name        varchar(150) not null,
    email            varchar(150),
    is_active        boolean not null default true,
    is_approved      boolean not null default true,
    created_at       timestamptz not null default now()
);

create unique index uq_user_account_email_lower
    on user_account ((lower(email)))
    where email is not null;

create index idx_user_account_role on user_account(role_id);
create index idx_user_account_username_lower on user_account ((lower(username)));
create index idx_user_account_full_name_lower on user_account ((lower(full_name)));

create table refresh_token (
    refresh_token_id  integer generated by default as identity primary key,
    user_account_id   integer not null references user_account(user_account_id) on delete cascade,
    token_value       varchar(128) not null unique,
    expires_at        timestamptz not null,
    created_at        timestamptz not null default now(),
    revoked_at        timestamptz
);

create index idx_refresh_token_user on refresh_token(user_account_id);

create table registration_request (
    request_id        integer generated by default as identity primary key,
    desired_role      varchar(20) not null check (desired_role in ('teacher', 'student')),
    username          varchar(50) not null unique,
    password_hash     varchar(100) not null,
    full_name         varchar(150) not null,
    email             varchar(150),
    status            varchar(20) not null default 'pending'
                      check (status in ('pending', 'approved', 'rejected')),
    created_at        timestamptz not null default now(),
    approved_by       integer references user_account(user_account_id),
    approved_at       timestamptz
);

create index idx_registration_request_status on registration_request(status);

create table department (
    department_id     integer generated by default as identity primary key,
    department_code   varchar(20) not null unique,
    department_name   varchar(150) not null unique
);

create table study_group (
    group_id              integer generated by default as identity primary key,
    group_code            varchar(20) not null unique,
    course_no             smallint not null check (course_no between 1 and 6),
    admission_year        smallint not null,
    faculty_name          varchar(150) not null,
    specialization_name   varchar(150) not null
);

create index idx_study_group_course_no on study_group(course_no);

create table student (
    student_id         integer generated by default as identity primary key,
    user_account_id    integer not null unique references user_account(user_account_id) on delete cascade,
    group_id           integer not null references study_group(group_id),
    student_card       varchar(20) not null unique
);

create index idx_student_group on student(group_id);
create index idx_student_student_card on student(student_card);

create table teacher (
    teacher_id         integer generated by default as identity primary key,
    user_account_id    integer not null unique references user_account(user_account_id) on delete cascade,
    department_id      integer not null references department(department_id),
    position           varchar(100) not null,
    phone              varchar(30)
);

create index idx_teacher_department on teacher(department_id);

create table academic_term (
    term_id            integer generated by default as identity primary key,
    academic_year      varchar(20) not null,
    term_no            smallint not null check (term_no in (1, 2)),
    start_date         date not null,
    end_date           date not null,
    is_current         boolean not null default false,
    unique (academic_year, term_no),
    check (start_date < end_date)
);

create table course (
    course_id          integer generated by default as identity primary key,
    course_code        varchar(20) not null unique,
    course_name        varchar(150) not null,
    description        text,
    credits            smallint not null check (credits > 0),
    total_hours        integer not null check (total_hours > 0),
    study_year         smallint not null check (study_year between 1 and 6),
    control_form       varchar(20) not null
                       check (control_form in ('exam', 'test', 'credit'))
);

create index idx_course_study_year on course(study_year);

-- ---------------------------------------------------------------------
-- 3. Учебный процесс
-- ---------------------------------------------------------------------
create table course_offering (
    offering_id        integer generated by default as identity primary key,
    course_id          integer not null references course(course_id) on delete cascade,
    teacher_id         integer not null references teacher(teacher_id),
    group_id           integer not null references study_group(group_id) on delete cascade,
    term_id            integer not null references academic_term(term_id),
    status             varchar(20) not null default 'active'
                       check (status in ('planned', 'active', 'completed')),
    unique (course_id, teacher_id, group_id, term_id)
);

create index idx_course_offering_group_term on course_offering(group_id, term_id);
create index idx_course_offering_teacher on course_offering(teacher_id);
create index idx_course_offering_course on course_offering(course_id);

create table schedule_entry (
    schedule_id        integer generated by default as identity primary key,
    offering_id        integer not null references course_offering(offering_id) on delete cascade,
    lesson_date        date not null,
    time_slot          varchar(20) not null,
    lesson_type        varchar(20) not null
                       check (lesson_type in ('lecture', 'practice', 'lab', 'seminar', 'exam', 'consultation')),
    room               varchar(20),
    topic              varchar(200),
    unique (offering_id, lesson_date, time_slot)
);

create index idx_schedule_entry_offering_date on schedule_entry(offering_id, lesson_date);

create table enrollment (
    enrollment_id      integer generated by default as identity primary key,
    student_id         integer not null references student(student_id) on delete cascade,
    offering_id        integer not null references course_offering(offering_id) on delete cascade,
    enrollment_date    date not null default current_date,
    missed_hours       integer not null default 0 check (missed_hours >= 0),
    current_status     varchar(20) not null default 'active'
                       check (current_status in ('active', 'completed', 'dropped')),
    unique (student_id, offering_id)
);

create index idx_enrollment_student on enrollment(student_id);
create index idx_enrollment_offering on enrollment(offering_id);

create table assessment_type (
    assessment_type_id integer generated by default as identity primary key,
    type_code          varchar(20) not null unique,
    type_name          varchar(100) not null,
    default_weight     numeric(5,2) not null check (default_weight >= 0 and default_weight <= 1),
    allows_multiple    boolean not null default true
);

create table assessment_item (
    assessment_item_id   integer generated by default as identity primary key,
    offering_id          integer not null references course_offering(offering_id) on delete cascade,
    assessment_type_id   integer not null references assessment_type(assessment_type_id),
    title                varchar(200) not null,
    max_score            numeric(5,2) not null default 10.00 check (max_score > 0),
    weight               numeric(5,2) not null check (weight >= 0 and weight <= 1),
    due_date             date not null,
    schedule_id          integer references schedule_entry(schedule_id) on delete set null,
    is_required          boolean not null default true
);

create index idx_assessment_item_offering on assessment_item(offering_id);
create index idx_assessment_item_schedule on assessment_item(schedule_id);

create table attendance (
    attendance_id      integer generated by default as identity primary key,
    enrollment_id      integer not null references enrollment(enrollment_id) on delete cascade,
    schedule_id        integer not null references schedule_entry(schedule_id) on delete cascade,
    is_present         boolean not null,
    note               text,
    unique (enrollment_id, schedule_id)
);

create index idx_attendance_enrollment on attendance(enrollment_id);
create index idx_attendance_schedule on attendance(schedule_id);

create table grade (
    grade_id           integer generated by default as identity primary key,
    enrollment_id      integer not null references enrollment(enrollment_id) on delete cascade,
    assessment_item_id integer not null references assessment_item(assessment_item_id) on delete cascade,
    grade_value        numeric(4,2) not null check (grade_value >= 0 and grade_value <= 10),
    teacher_comment    text,
    graded_at          timestamptz not null default now(),
    unique (enrollment_id, assessment_item_id)
);

create index idx_grade_enrollment on grade(enrollment_id);
create index idx_grade_assessment_item on grade(assessment_item_id);

-- ---------------------------------------------------------------------
-- 4. Прогнозирование и рекомендации
-- ---------------------------------------------------------------------
create table performance_prediction (
    prediction_id            integer generated by default as identity primary key,
    enrollment_id            integer not null references enrollment(enrollment_id) on delete cascade,
    current_average          numeric(4,2) not null check (current_average >= 0 and current_average <= 10),
    attendance_rate          numeric(5,2) not null check (attendance_rate >= 0 and attendance_rate <= 100),
    missed_hours             integer not null check (missed_hours >= 0),
    predicted_final_grade    numeric(4,2) not null check (predicted_final_grade >= 0 and predicted_final_grade <= 10),
    risk_level               varchar(20) not null check (risk_level in ('low', 'medium', 'high')),
    confidence_score         numeric(5,2) not null check (confidence_score >= 0 and confidence_score <= 1),
    missing_required_count   integer not null default 0 check (missing_required_count >= 0),
    recommendation_summary   text,
    model_version            varchar(50) not null,
    generated_at             timestamptz not null default now()
);

create index idx_prediction_enrollment on performance_prediction(enrollment_id);

create table student_recommendation (
    recommendation_id     integer generated by default as identity primary key,
    enrollment_id         integer not null references enrollment(enrollment_id) on delete cascade,
    prediction_id         integer references performance_prediction(prediction_id) on delete cascade,
    recommendation_type   varchar(30) not null
                          check (recommendation_type in ('attendance', 'performance', 'assignment', 'support')),
    recommendation_text   text not null,
    priority              smallint not null check (priority between 1 and 5),
    status                varchar(20) not null default 'new'
                          check (status in ('new', 'in_progress', 'done', 'dismissed')),
    created_at            timestamptz not null default now()
);

create index idx_recommendation_prediction on student_recommendation(prediction_id);
create index idx_recommendation_enrollment on student_recommendation(enrollment_id);

