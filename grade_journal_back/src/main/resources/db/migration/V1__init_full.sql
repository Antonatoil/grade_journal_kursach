-- 5. Триггеры для учета пропущенных часов
-- ---------------------------------------------------------------------
create or replace function fn_update_missed_hours_after_insert()
returns trigger
language plpgsql
as $$
begin
    if new.is_present = false then
        update enrollment
        set missed_hours = missed_hours + 2
        where enrollment_id = new.enrollment_id;
    end if;
    return new;
end;
$$;

create trigger trg_attendance_after_insert
after insert on attendance
for each row
execute function fn_update_missed_hours_after_insert();

create or replace function fn_update_missed_hours_after_update()
returns trigger
language plpgsql
as $$
begin
    if old.is_present = true and new.is_present = false then
        update enrollment
        set missed_hours = missed_hours + 2
        where enrollment_id = new.enrollment_id;
    elsif old.is_present = false and new.is_present = true then
        update enrollment
        set missed_hours = greatest(missed_hours - 2, 0)
        where enrollment_id = new.enrollment_id;
    end if;

    return new;
end;
$$;

create trigger trg_attendance_after_update
after update on attendance
for each row
execute function fn_update_missed_hours_after_update();

create or replace function fn_update_missed_hours_after_delete()
returns trigger
language plpgsql
as $$
begin
    if old.is_present = false then
        update enrollment
        set missed_hours = greatest(missed_hours - 2, 0)
        where enrollment_id = old.enrollment_id;
    end if;

    return old;
end;
$$;

create trigger trg_attendance_after_delete
after delete on attendance
for each row
execute function fn_update_missed_hours_after_delete();

