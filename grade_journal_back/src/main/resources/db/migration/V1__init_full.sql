insert into teacher (user_account_id, department_id, position, phone)
select
    ua.user_account_id,
    ((row_number() over (order by ua.user_account_id) - 1) % 4) + 1 as department_id,
    case
        when row_number() over (order by ua.user_account_id) <= 5 then 'Профессор'
        when row_number() over (order by ua.user_account_id) <= 10 then 'Доцент'
        else 'Старший преподаватель'
    end as position,
    '+37529' || lpad((1000000 + row_number() over (order by ua.user_account_id))::text, 7, '0')
from user_account ua
join role r on r.role_id = ua.role_id
where r.role_code = 'teacher'
order by ua.user_account_id;

with student_users as (
    select
        ua.user_account_id,
        row_number() over (order by ua.user_account_id) as rn
    from user_account ua
    join role r on r.role_id = ua.role_id
    where r.role_code = 'student'
),
student_groups as (
    select
        su.user_account_id,
        ((su.rn - 1) / 12) + 1 as group_id,
        row_number() over (
            partition by ((su.rn - 1) / 12) + 1
            order by su.user_account_id
        ) as idx_in_group
    from student_users su
)
insert into student (user_account_id, group_id, student_card)
select
    sg.user_account_id,
    sg.group_id,
    gr.group_code || lpad(sg.idx_in_group::text, 2, '0')
from student_groups sg
join study_group gr on gr.group_id = sg.group_id
order by sg.user_account_id;

